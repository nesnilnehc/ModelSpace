(function buildCognitiveAtlasObjects() {
  const relationIndex = {};
  for (const edge of window.COGNITIVE_ATLAS_RELATIONS) {
    if (!edge?.source) continue;
    if (!relationIndex[edge.source]) relationIndex[edge.source] = [];
    relationIndex[edge.source].push({ type: edge.type, target: edge.target });
  }

  const byNameOverrides = {
    SWOT: "Framework",
    AARRR: "Framework",
    OKR: "System",
    KPI: "Metric",
    "First Principles": "Principle",
    "Systems Thinking": "Framework",
    Flywheel: "Model",
    "Lean Startup": "Method"
  };

  function compact(text) {
    return String(text || "").trim().toLowerCase();
  }

  function inferObjectType(name, descriptionEn, category, zOverride) {
    const byName = byNameOverrides[name];
    if (byName) return byName;
    const text = `${compact(name)} ${compact(descriptionEn)}`;
    if (/\b(metric|kpi|measure|indicator|score)\b/.test(text)) return "Metric";
    if (/\b(system|operating system|governance)\b/.test(text)) return "System";
    if (/\b(framework|canvas|matrix)\b/.test(text)) return "Framework";
    if (/\b(theory)\b/.test(text)) return "Theory";
    if (/\b(pattern|loop)\b/.test(text)) return "Pattern";
    if (/\b(heuristic|rule of thumb|razor)\b/.test(text)) return "Heuristic";
    if (/\b(principle)\b/.test(text)) return "Principle";
    if (/\b(method|process|step|cycle)\b/.test(text)) return "Method";
    if (/\b(model)\b/.test(text)) return "Model";
    if (/\b(template|tool|checklist)\b/.test(text)) return "Tool";
    if (Number.isFinite(zOverride)) {
      if (zOverride === 1) return "Tool";
      if (zOverride === 2) return "Method";
      if (zOverride === 3) return "Principle";
      if (zOverride === 4) return "Framework";
    }
    return {
      Expression: "Method",
      Structure: "Framework",
      Diagnosis: "Method",
      Strategy: "Framework",
      Meta: "Principle"
    }[category] || "Framework";
  }

  function inferLifecycle(name, descriptionEn, category) {
    const text = `${compact(name)} ${compact(descriptionEn)}`;
    if (/\b(retrospective|postmortem|root cause|diagnos)\b/.test(text)) return 2;
    if (/\b(plan|priorit|strategy|roadmap|design)\b/.test(text)) return 3;
    if (/\b(execute|action|operate|implementation)\b/.test(text)) return 4;
    if (/\b(learn|adapt|feedback|improve|iteration)\b/.test(text)) return 5;
    if (/\b(explain|sensemaking|understand)\b/.test(text)) return 1;
    return { Expression: 1, Structure: 2, Diagnosis: 2, Strategy: 3, Meta: 5 }[category] || 3;
  }

  function inferAbstraction(objectType) {
    return {
      Theory: 2,
      Principle: 2,
      Framework: 3,
      Model: 3,
      Pattern: 3,
      Method: 4,
      Heuristic: 4,
      Tool: 5,
      Metric: 5,
      System: 3
    }[objectType] || 3;
  }

  function inferScale(name, descriptionEn, category) {
    const text = `${compact(name)} ${compact(descriptionEn)}`;
    if (/\b(personal|individual|learning|memory)\b/.test(text)) return 1;
    if (/\b(team|meeting|collaborat)\b/.test(text)) return 2;
    if (/\b(organization|business|company|strategy|portfolio)\b/.test(text)) return 3;
    if (/\b(market|industry|ecosystem|customer)\b/.test(text)) return 4;
    if (/\b(policy|society|public|global)\b/.test(text)) return 5;
    return { Expression: 2, Structure: 2, Diagnosis: 3, Strategy: 3, Meta: 2 }[category] || 3;
  }

  function slugify(text) {
    return String(text || "").toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-+|-+$/g, "");
  }

  const coordinatesByName = window.MODEL_COORDINATES_BY_NAME || {};

  window.COGNITIVE_ATLAS_OBJECTS = window.MODEL_LIBRARY_ROWS.map((row) => {
    const [name, aliasZh, descriptionEn, category, yOverride, zOverride] = row;
    const evaluation = window.MODEL_EVALUATION_BY_NAME?.[name]?.evaluation || window.MODEL_EVALUATION_META.default;
    const objectType = inferObjectType(name, descriptionEn, category, zOverride);
    const explicitCoords = coordinatesByName[name];

    let atlasV2;
    let axisRationale = null;

    if (evaluation?.stageA === "不纳入") {
      atlasV2 = { classificationStatus: "excluded", admitted: false, reason: "stageA_rejected" };
    } else if (explicitCoords?.coordinates) {
      const { x, y, z } = explicitCoords.coordinates;
      atlasV2 = {
        classificationStatus: "classified",
        admitted: true,
        objectType,
        coordinates: {
          primary: { x, y, z },
          axes: {
            xName: "DecisionLifecycle",
            yName: "AbstractionLevel",
            zName: "ApplicationScale"
          }
        },
        confidence: 1
      };
      axisRationale = explicitCoords.axisRationale || null;
    } else {
      atlasV2 = {
        classificationStatus: "classified",
        admitted: true,
        objectType,
        coordinates: {
          primary: {
            x: inferLifecycle(name, descriptionEn, category),
            y: inferAbstraction(objectType),
            z: inferScale(name, descriptionEn, category)
          },
          axes: {
            xName: "DecisionLifecycle",
            yName: "AbstractionLevel",
            zName: "ApplicationScale"
          }
        },
        confidence: 1
      };
    }

    return {
      id: `ca.${slugify(objectType)}.${slugify(name)}.v1`,
      name,
      aliasZh,
      aliases: [aliasZh].filter(Boolean),
      descriptionEn,
      category,
      objectType,
      evaluation,
      atlasV2,
      axisRationale,
      purpose: descriptionEn,
      references: window.MODEL_REFERENCE_RESOURCES?.[name] || null,
      relations: relationIndex[name] || []
    };
  });
})();
